public class SwapNumber {


    public static void main(String[] args) {

        int a = 5 ;
        int b = 10;

        System.out.println( "a = " + a);
        System.out.println( "b = " + b);
        System.out.println( " after swaping number ");



        int sp  = a ;
        a=b;
        b=sp;


        System.out.println("a : = " + a );
        System.out.println( "b = " + b);


    }
}
